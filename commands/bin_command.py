def fetch_bin_info(bin_number):
    conn = sqlite3.connect('bins.db')
    cursor = conn.cursor()
    cursor.execute('SELECT * FROM bins WHERE bin = ?', (bin_number,))
    result = cursor.fetchone()
    conn.close()
    if result:
        return {
            'Brand': result[1],
            'Type': result[2],
            'Category': result[3],
            'Issuer': result[4],
            'CountryName': result[9],
            'iso_code2': result[7]
        }
    return None

def format_bin_info(bin_data, bin_number=None):
    if not bin_data:
        return None
    
    brand = bin_data.get('Brand', 'Unknown')
    type_ = bin_data.get('Type', 'Unknown')
    category = bin_data.get('Category', 'Unknown')
    bank = bin_data.get('Issuer', 'Unknown')
    country = bin_data.get('CountryName', 'Unknown')
    country_code = bin_data.get('iso_code2', '')
    flag = get_flags(country_code)
    
    return f"""Bin: <b>{brand} - {type_} - {category}</b>
Bank: <b>{bank}</b>
Country: <b>{country} - {flag}</b>"""

def register_bin_command(bot):
    @bot.message_handler(commands=['bin'])
    async def bin_command(message):
        try:
            command, *args = message.text[1:].split()
            command = command.split('@')[0]

            user_id = message.from_user.id
            user = get_user(user_id)
            
            user_link = f'<a href="tg://user?id={user_id}">{message.from_user.first_name}</a>'
            user_type = "Owner" if user.is_admin else "Premium User" if user.is_premium else "Free User"

            if not user:
                user = add_user(user_id)
                user = get_user(user_id)
                if not user:
                    await bot.reply_to(message, "Failed to register user.")
                    return
            
            try:
                bin_number = args[0][:6]
            except IndexError:
                await bot.reply_to(message, "<b>Please provide a BIN number.<b>")
                return
            except:
                await bot.reply_to(message, "<b>Please provide a BIN number.<b>")
            
            bin_data = fetch_bin_info(bin_number)

            if not bin_data:
                await bot.reply_to(message, "<b>Invalid Bin.<b>")
                return
            
            bin_info = format_bin_info(bin_data, bin_number)
            if not bin_info:
                await bot.reply_to(message, "Failed to format BIN info")
                return
            
            bin_info += f"\n<b>Checked By: {user_link} [{user_type}]</b>"

            await bot.reply_to(message, bin_info)
        except Exception as e:
            await bot.reply_to(message, f"Error: {str(e)}")
